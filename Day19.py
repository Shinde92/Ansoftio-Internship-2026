# Define functions (like module)

def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b


# Main program
if __name__ == "__main__":
    
    # Call functions
    result1 = addition(10, 5)
    result2 = subtraction(10, 5)

    # Print results
    print("Addition:", result1)
    print("Subtraction:", result2)
